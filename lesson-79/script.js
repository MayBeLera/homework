// Четные числа от 2 до 12 с помощью for
for(let i = 2; i <= 12; i+=2){
    console.log(i)
}
// числа от 10 до 1  помощью for
for(let i = 10; i >= 1; i--){
    console.log(i)
}

// Четные числа от 2 до 12 с помощью while
let i = 0;
while(i < 12){
    i += 2
    console.log(i)
}
// числа от 10 до 1 с помощью while
let a = 11;
while(a > 1){
    a--
    console.log(a)
}







